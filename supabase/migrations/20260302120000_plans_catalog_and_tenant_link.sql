-- Multi-tenant SaaS plan catalog + tenant plan assignment
-- Idempotent migration: safe to run multiple times.

CREATE TABLE IF NOT EXISTS public.plans (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL UNIQUE,
  price_monthly NUMERIC(12,2) NOT NULL DEFAULT 0,
  max_cadastros INTEGER NOT NULL CHECK (max_cadastros >= 0),
  features JSONB NOT NULL DEFAULT '{}'::jsonb,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT plans_name_check CHECK (name IN ('standard', 'simple', 'explorer', 'enterprise')),
  CONSTRAINT plans_features_is_object CHECK (jsonb_typeof(features) = 'object')
);

CREATE INDEX IF NOT EXISTS idx_plans_name ON public.plans(name);

-- Add plan_id without changing existing tenant status/circuit-breaker semantics.
ALTER TABLE public.tenants
  ADD COLUMN IF NOT EXISTS plan_id BIGINT;

-- Ensure FK exists (idempotent with catalog check).
DO $$
BEGIN
  IF NOT EXISTS (
    SELECT 1
    FROM pg_constraint
    WHERE conname = 'tenants_plan_id_fkey'
      AND conrelid = 'public.tenants'::regclass
  ) THEN
    ALTER TABLE public.tenants
      ADD CONSTRAINT tenants_plan_id_fkey
      FOREIGN KEY (plan_id)
      REFERENCES public.plans(id)
      ON UPDATE RESTRICT
      ON DELETE RESTRICT;
  END IF;
END
$$;

CREATE INDEX IF NOT EXISTS idx_tenants_plan_id ON public.tenants(plan_id);

