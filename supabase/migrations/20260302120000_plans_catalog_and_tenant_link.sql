UPDATE public.tenants t
SET plan_id = p.id
FROM public.plans p
WHERE t.plan_id IS NULL
  AND p.name = 'simple';

-- For future inserts, default to "simple" without hardcoding app-side inserts.
DO $$
DECLARE
  v_simple_plan_id BIGINT;
BEGIN
  SELECT id INTO v_simple_plan_id
  FROM public.plans
  WHERE name = 'simple'
  LIMIT 1;

  IF v_simple_plan_id IS NOT NULL THEN
    EXECUTE format(
      'ALTER TABLE public.tenants ALTER COLUMN plan_id SET DEFAULT %s',
      v_simple_plan_id
    );
  END IF;
END
$$;

-- Enforce assignment after backfill.
DO $$
BEGIN
  IF EXISTS (
    SELECT 1
    FROM information_schema.columns
    WHERE table_schema = 'public'
      AND table_name = 'tenants'
      AND column_name = 'plan_id'
      AND is_nullable = 'YES'
  ) THEN
    ALTER TABLE public.tenants
      ALTER COLUMN plan_id SET NOT NULL;
  END IF;
END
$$;

-- RLS compatibility: keep tenant-isolation behavior and expose read-only plan catalog.
ALTER TABLE public.plans ENABLE ROW LEVEL SECURITY;

